-- ============================================================
-- session_user_traits: セッション×ユーザーごとの個性記録
-- ============================================================
CREATE TABLE IF NOT EXISTS session_user_traits (
  id          uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  session_id  uuid        REFERENCES liff_sessions(id) ON DELETE CASCADE,
  user_id     uuid        REFERENCES liff_users(id)    ON DELETE CASCADE,
  trait_id    uuid        REFERENCES personality_traits(id) ON DELETE CASCADE,
  created_at  timestamptz NOT NULL DEFAULT now(),
  UNIQUE(session_id, user_id, trait_id)
);
